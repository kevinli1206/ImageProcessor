  @Test
  public void testGetHistorgramThrowsIllegalArgument() {
    this.model1.loadImage("twoByTwo", this.twoByTwo);
    try {
      this.model1.getHistogram("twoByOne");
      fail();
    } catch (IllegalArgumentException e) {
      assertEquals("Image cannot be found", e.getMessage());
    }
    this.model1.getHistogram("twoByTwo");
  }

  @Test
  public void testGetHistogramReturnsRightSize() {
    this.model1.loadImage("twoByTwo", this.twoByTwo);
    List<IHistogram> loh = this.model1.getHistogram("twoByTwo");
    assertEquals(4, loh.size());
    this.model1.greyscaleComponent(Light.RED, "twoByTwo", "grayTwoByTwo");
    List<IHistogram> loh2 = this.model1.getHistogram("grayTwoByTwo");
    assertEquals(1, loh2.size());
  }

